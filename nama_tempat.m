    case 8
        tempat = 'Candi Prambanan';
    case 9
        tempat = 'Museum Batik Danar Hadi';
    case 10
        tempat = 'Gunung Penanjakan';
    case 11
        tempat = 'Air Terjun Madakaripura';
    case 12
        tempat = 'Pantai Sukamade';
    case 13
